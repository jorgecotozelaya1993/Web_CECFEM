con el fin de mejorar el aprendizaje de los niños en el nivel de Educación Básica, mediante el desarrollo de nuevas prácticas docentes basadas en la 
aplicación de tecnologías a la enseñanza. Posteriormente, los CRA pasan a ser llamados Aulas Informáticas (AI), al pasar a formar parte de los componentes
