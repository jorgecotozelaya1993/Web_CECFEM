        <p align="justify" style="padding-left:6px; font-family:Arial, Helvetica, sans-serif; font-size:15px; font-weight: bold; margin-top:15px; margin-right:15px;">La crisis de identidad de la Educación Media, al comenzar la última década del siglo, no fue un problema eminentemente local, puesto que similares dificultades se advierten en el orden  internacional. Esta situación demando el desarrollo de numerosos eventos internacionales para debatir y resolver sus problemas reales y conceptuales.</p><br>
