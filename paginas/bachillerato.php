        <p align="justify" style="padding-left:6px; font-family:Arial, Helvetica, sans-serif; font-size:15px; font-weight: bold; margin-top:15px; margin-right:15px;">La Educación Media es la etapa de formación del educando que sucede a la Educación  Básica y cuyo propósito es facilitar al joven y a la joven una mejor comprensión  de  sí  mismo,  el  desarrollo  de  competencias  que  les  permitan enfrentarse a  un  mundo,  acceder a  la  estructura  productiva  y  la participación efectiva en una sociedad pluralista y democrática.</p><br>
