		<li>Fomentar la formación de buenos hábitos.</li>
		<li>Estimular y guiar las experiencias infantiles.</li>
        <li>Cultivar el sentido estético.</li>
        <li>Desarrollar actitudes de compañerismo y cooperación.</li>
