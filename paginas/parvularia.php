        </tr>
        <tr>
        <td>
        <p align="left" style="padding-left:6px; font-family:Arial, Helvetica, sans-serif; font-size:16px; font-weight: bold; margin-top:15px; margin-right: 15px">La educación Parvularia tiene como objetivo principal, adaptar al niño a una futura educación formal, además el gobierno a través de la “Ley General de Educación de la República de El Salvador Titulo II, Capítulo III, Art. 19, define los siguientes objetivos:</p><br>
        <ol style="font-family:Arial; font-size:16; color:#000; font-weight:bold; list-style:lower-roman; padding-left:25px; padding-right:10px; color:#039">
        <li>Estimular  el  desarrollo  integral  de  los  educandos,  por  medio  de  procesos pedagógicos que tomen en cuenta su naturaleza psicomotora, efectiva y social.</li>
		<li>Fortalecer  la  identidad  y  la  autoestima  de  los  educandos  como  condición necesaria para  el desarrollo de sus potencialidades en sus espacios vitales, familia, escuela y comunidad.</li>
		<li>Desarrollar  las  especialidades  básicas  de  los  educandos  para  garantizar  su adecuada preparación e incorporación a la educación básica.</li>
        </ol>
        <p align="left" style="padding-left:6px; font-family:Arial, Helvetica, sans-serif; font-style:italic; font-size:18px; font-weight: bold; margin-top:30px">AREA COGNITIVA:</p><br>
        <p align="justify" style="font-family:Arial; font-size:16; color:#000; font-weight:bold; margin-left:25px; margin-right:10px"><b>Representar el conjunto de procesos por medio de los cuales el niño y la niña organiza mentalmente los datos de la realidad que le rodea y que los recibe a través de los sistemas sensoperceptual y propioceptivo para resolver situaciones nuevas, con base en experiencias pasadas.</b></p>
        <p align="left" style="padding-left:6px; font-family:Arial, Helvetica, sans-serif; font-style:italic; font-size:18px; font-weight: bold; margin-top:30px">AREA SOCIOAFECTIVA:</p><br>
        <p align="justify" style="font-family:Arial; font-size:16; color:#000; font-weight:bold; margin-left:25px; margin-right:10px"><b>Socializar al niño y a la niña que aprenda las reglas fundamentales para su integración al medio social y se desarrolle emocional y afectivamente; dependiendo del conocimiento que va adquiriendo sobre su persona, su cuerpo y sus facultades.</b></p>
        <p align="left" style="padding-left:6px; font-family:Arial, Helvetica, sans-serif; font-style:italic; font-size:18px; font-weight: bold; margin-top:30px">AREA BIOSICOMOTORA:</p><br>
        <p align="justify" style="font-family:Arial; font-size:16; color:#000; font-weight:bold; margin-left:25px; margin-right:10px"><b>Interaccionar con los objetos y las personas de su entorno, niños y niñas aprenderán a responder mejor, de modo que las conductas antes reflejas se irán complejizando, haciéndose más y adecuadas.</b></p>
        </td>
